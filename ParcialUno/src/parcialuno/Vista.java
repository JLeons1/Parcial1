    private void agregarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarBtnActionPerformed
    String datos[] = new String[5];
        datos[0] = (String) subtipoCombo.getSelectedItem();
        if (datos[0]=="Aereo")
        {
            if (avionButton.isSelected()){
                
            datos[1] = avionButton.getText();
            
            } else {
                
                datos[1] = heliButton.getText();
            }
            
        }
               if (datos[0]=="Terrestre")
        {
            if (carroButton.isSelected()){
                
            datos[1] = carroButton.getText();
            
            } else {
                
                datos[1] = motoButton.getText();
            }
             
        }
        datos[2] = numeroserieTxt.getText();
        datos[3] = marcaTxt.getText();
        datos[4] = modeloTxt.getText();  
        t_vehiculo.addRow(datos);
        JOptionPane.showMessageDialog(rootPane, "Ingreso Correcto","Mensaje",JOptionPane.INFORMATION_MESSAGE);
       
                // TODO add your handling code here:
    }//GEN-LAST:event_agregarBtnActionPerformed

