    private void subtipoComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_subtipoComboActionPerformed
       String vehiculo;
       vehiculo = (String) subtipoCombo.getSelectedItem();
       if (vehiculo == "Terrestre"){
           
       carroButton.setEnabled(true);
       motoButton.setEnabled(true);
       avionButton.setEnabled(false);
       heliButton.setEnabled(false);
       
       }else if (vehiculo == "Aereo"){
           
       carroButton.setEnabled(false);
       motoButton.setEnabled(false);
       avionButton.setEnabled(true);
       heliButton.setEnabled(true);
       
       }
    }//GEN-LAST:event_subtipoComboActionPerformed

